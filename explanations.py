def help_text(key: str) -> str:
    """Текст за Streamlit tooltip с връзка към подробната страница."""

    item = EXPLANATIONS[key]
    topic = quote(key)
    return f"{item['short']}\n\n[Отвори подробно](?page=models&topic={topic})"


def explanation_titles() -> dict[str, str]:
    return {key: value["title"] for key, value in EXPLANATIONS.items()}
